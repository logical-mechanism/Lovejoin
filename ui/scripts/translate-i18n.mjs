// Note: protocol.* keys were translated by a prior commit on this branch
// (see git log for `i18n: translate rewritten protocol page`). Those
// translations live in the locale files directly; this script only
// covers the keys that were still missing after that pass. Future
// translation runs should extend TRANSLATIONS above with whatever new
// keys land in en.json.

const localeFiles = Object.keys(TRANSLATIONS).map((code) => ({
  code,
  path: resolve(LOCALES_DIR, `${code}.json`),
}));

let totalUpdates = 0;
for (const { code, path } of localeFiles) {
  const raw = await readFile(path, "utf8");
  const data = JSON.parse(raw);
  const before = JSON.stringify(data);
  for (const [flatKey, value] of Object.entries(TRANSLATIONS[code])) {
    const dot = flatKey.indexOf(".");
    const section = flatKey.slice(0, dot);
    const key = flatKey.slice(dot + 1);
    if (!data[section]) data[section] = {};
    data[section][key] = value;
  }
  const after = JSON.stringify(data);
  if (before !== after) {
    await writeFile(path, JSON.stringify(data, null, 2) + "\n", "utf8");
    totalUpdates += 1;
    console.log(`translate-i18n: updated ${code}.json`);
  }
}

console.log(`translate-i18n: ${totalUpdates} locale(s) updated.`);
