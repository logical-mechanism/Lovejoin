const SUPPORTED = Object.keys(TRANSLATIONS);

async function patchOne(code) {
  const path = `${LOCALES_DIR}/${code}.json`;
  const raw = await readFile(path, "utf8");
  const data = JSON.parse(raw);
  data.vault = data.vault ?? {};
  // Merge: preserve every key already in vault.seedelf (the #135 read-only
  // strings) and add the v2 keys. Re-running is a no-op.
  data.vault.seedelf = { ...(data.vault.seedelf ?? {}), ...TRANSLATIONS[code] };
  await writeFile(path, JSON.stringify(data, null, 2) + "\n", "utf8");
}

async function main() {
  for (const code of SUPPORTED) {
    await patchOne(code);
    process.stdout.write(`  updated ${code}.json\n`);
  }
}

await main();
